def clean_msa(
    raw_a3m_path,
    clean_a3m_path,
    valid_taxid_set,
    max_valid_tax=10000,
    max_orphan=None,
):
    """
    Create a cleaned A3M file (targets only) by keeping:
      - valid-tax sequences (TaxID in valid_taxid_set), capped by max_valid_tax
      - orphan sequences (missing/invalid TaxID), optionally capped by max_orphan

    IMPORTANT:
        This function writes ONLY target pairs (no query pair).
        The query sequence will be re-added when exporting cluster A3Ms.

    Args:
        raw_a3m_path: str, original A3M containing query + targets
        clean_a3m_path: str, output cleaned A3M path (targets only)
        valid_taxid_set: set[int], allowed TaxIDs
        max_valid_tax: int or None
        max_orphan: int or None
    """
    with open(raw_a3m_path, "r") as f:
        lines = f.readlines()

    pairs = np.array(lines).reshape(-1, 2)
    target_pairs = pairs[1:]  # skip query pair

    valid_pairs = []
    orphan_pairs = []

    for pair in target_pairs:
        header = pair[0]
        is_valid = False

        if "TaxID=" in header:
            taxid = header.split("TaxID=")[-1].split()[0]
            if str(taxid).isdigit() and int(taxid) in valid_taxid_set:
                is_valid = True

        if is_valid:
            valid_pairs.append(pair)
        else:
            orphan_pairs.append(pair)

    if max_valid_tax is not None and len(valid_pairs) > max_valid_tax:
        valid_pairs = valid_pairs[:max_valid_tax]

    if max_orphan is not None and len(orphan_pairs) > max_orphan:
        orphan_pairs = orphan_pairs[:max_orphan]

    out_pairs = valid_pairs + orphan_pairs
    out_lines = np.array(out_pairs).reshape(-1).tolist() if len(out_pairs) > 0 else []

    with open(clean_a3m_path, "w") as fw:
        fw.writelines(out_lines)


