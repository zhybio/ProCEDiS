def extract_ca_bfactors(pdb_file):
    """
    Extract B-factors for all C-alpha atoms in a PDB file.
    In AlphaFold/OpenFold outputs, C-alpha B-factors typically store pLDDT scores.
    """
    parser = PDBParser(QUIET=True)
    structure = parser.get_structure("structure", pdb_file)

    ca_bfactors = []
    for model in structure:
        for chain in model:
            for residue in chain:
                if "CA" in residue.child_dict:
                    ca_atom = residue["CA"]
                    ca_bfactors.append(ca_atom.get_bfactor())
    return ca_bfactors


def prepare_init_data(init_dir, pdb_list, clusters, plddt_threshold, rmsd_matrix):
    """
    Select initial seed indices by:
      - filtering by mean pLDDT
      - clustering with RMSD threshold
      - taking highest pLDDT representative per cluster

    Output:
        init_idx.npy under init_dir
    """
    traj = md.load(copy.copy(pdb_list))

    # Compute mean pLDDT for each structure
    plddts = []
    for fp in pdb_list:
        p = extract_ca_bfactors(fp)
        plddts.append(p)
    plddts = (np.stack(plddts) / 100.0).mean(axis=-1)

    filted_idx = np.where(plddts > plddt_threshold)[0]

    rmsds = rmsd_matrix[filted_idx, :][:, filted_idx].numpy()

    rmsd_threshold = 0.28 * np.sqrt(traj.top.n_residues)
    if len(rmsds) >= 2:
        cluster = AgglomerativeClustering(
            n_clusters=None,
            metric="precomputed",
            linkage="complete",
            distance_threshold=rmsd_threshold,
        )
        labels = cluster.fit_predict(rmsds)
    else:
        labels = np.array([0])

    non_redundant_idx = []
    for label in range(labels.max() + 1):
        label_idx = np.where(labels == label)[0]
        if len(label_idx) <= 1:
            continue
        plddts_label = plddts[filted_idx][label_idx]
        non_redundant_idx.append(filted_idx[label_idx[np.argmax(plddts_label)]])

    init_idx = np.array(non_redundant_idx).astype(int)
    np.save(os.path.join(init_dir, "init_idx.npy"), init_idx)
