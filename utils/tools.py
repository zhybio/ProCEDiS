ALPHABET = "ACDEFGHIKLMNPQRSTVWY-"
ALPHABET_DICT = {res: i for i, res in enumerate(ALPHABET)}
NUM_TOKENS = len(ALPHABET)


def encode_seqs(seqs, max_len=108, alphabet=None):
    """
    One-hot encode a list of sequences into a flattened array.

    Args:
        seqs: List[str], sequences (upper-case residues and '-' gaps).
        max_len: int, maximum sequence length used for encoding.
        alphabet: str, alphabet characters used for encoding.

    Returns:
        np.ndarray of shape [N, max_len * len(alphabet)]
    """
    if alphabet is None:
        alphabet = ALPHABET

    arr = np.zeros([len(seqs), max_len, len(alphabet)], dtype=np.float32)
    for j, seq in enumerate(seqs):
        for i, char in enumerate(seq):
            for k, res in enumerate(alphabet):
                if char == res:
                    arr[j, i, k] += 1.0
    return arr.reshape([len(seqs), max_len * len(alphabet)])


def generate_embed(a3m_path, embed_path):
    """
    Build simple one-hot embeddings for sequences in an A3M file and save as .npy.

    Important:
        This function expects `a3m_path` to be the cleaned A3M generated by `clean_msa`,
        i.e., it should contain ONLY target (header, sequence) pairs (no query pair).

    Args:
        a3m_path: str, cleaned A3M path containing target pairs only.
        embed_path: str, output path to save embeddings (.npy).
    """
    with open(a3m_path, "r") as f:
        lines = f.readlines()

    # lines: [header0, seq0, header1, seq1, ...]
    seqs = lines[1::2]
    seqs = ["".join([x for x in s if x.isupper() or x == "-"]) for s in seqs]

    embed = encode_seqs(seqs, max_len=len(seqs[0]))
    np.save(embed_path, embed)


