def replace_check(scores, rmsd_threshold):
    mask = scores < rmsd_threshold
    indices = torch.nonzero(mask, as_tuple=False).squeeze()
    if indices.numel() == 1:
        return indices.item()
    else:
        return False


def share_buffer(envs, rewarder, num_buffer, rmsd_threshold):
    buffer_pool = envs[0].buffer[:num_buffer].copy()
    new_num_buffer = num_buffer
    for env in envs:
        if len(env.buffer) > num_buffer:
            for _, frame in env.buffer[num_buffer:].iterrows():
                if len(buffer_pool) > 0:
                    scores = rewarder.calculate(frame["structure"], np.stack(buffer_pool["structure"]))
                    score = scores.min().item()
                    if score > rmsd_threshold:
                        buffer_pool = pd.concat([buffer_pool, frame.to_frame().T], ignore_index=True)
                        new_num_buffer += 1
                else:
                    buffer_pool = pd.concat([buffer_pool, frame.to_frame().T], ignore_index=True)
                    new_num_buffer += 1
    return buffer_pool, new_num_buffer


def shifted_tanh(x, threshold, sharpness=1.0):
    return torch.tanh((x - threshold) * sharpness)


def encode_batch_msa(msa_list):
    """
    Encode a batch of MSAs:
      - one-hot each sequence (L x 21),
      - flatten to (L*21),
      - average over sequences within each MSA.

    Args:
        msa_list: List[List[str]], each inner list contains sequences only (no headers).

    Returns:
        torch.Tensor of shape [batch, L*21]
    """
    seq_counts = [len(msa) for msa in msa_list]
    max_len = len(msa_list[0][0])

    total_seqs = sum(seq_counts)
    arr = np.full((total_seqs, max_len), fill_value=ALPHABET_DICT["-"], dtype=np.int64)

    offset = 0
    for msa in msa_list:
        for seq in msa:
            for j, char in enumerate(seq):
                if char in ALPHABET_DICT:
                    arr[offset, j] = ALPHABET_DICT[char]
            offset += 1

    one_hot = np.eye(NUM_TOKENS, dtype=np.float32)[arr]
    one_hot = one_hot.reshape(total_seqs, -1)

    embeddings = []
    offset = 0
    for count in seq_counts:
        emb = torch.tensor(one_hot[offset:offset + count]).mean(dim=0)
        embeddings.append(emb)
        offset += count

    return torch.stack(embeddings)


def get_all_hypo_embeddings(env):
    base = clean_sequences(env.msa)
    msa_list = []
    for cluster in env.availables:
        combined = base + cluster
        msa_list.append(clean_sequences(combined))
    return encode_batch_msa(msa_list)


def clean_sequences(seq_list):
    cleaned = []
    for line in seq_list:
        if line.startswith(">"):
            continue
        cleaned.append("".join(c for c in line.strip() if c.isupper() or c == "-"))
    return cleaned


